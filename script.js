function renderTestMode() {
  const weekData = getCurrentWeekData();
  testQuestionsEl.innerHTML = "";

  if (!testStarted) {
    testQuestionsEl.innerHTML = `<p>Click <strong>Configure & Start Test</strong> to begin.</p>`;
    resetTestBtn.classList.add("hidden");
    testProgressEl.classList.add("hidden");
    startTestBtn.classList.remove("hidden");
    updateTestingLayout();
    return;
  }

  resetTestBtn.classList.remove("hidden");
  testProgressEl.classList.remove("hidden");
  startTestBtn.classList.add("hidden");
  updateTestingLayout();

  if (activeQuestionIndex >= testQuestionPool.length) {
    submitTest();
    return;
  }

  const answeredCount = testQuestionPool.filter((item) => typeof userAnswers[item.id] === "number").length;
  const remainingCount = testQuestionPool.length - answeredCount;
  testProgressEl.textContent = `Question ${activeQuestionIndex + 1} of ${testQuestionPool.length}`;

  const q = testQuestionPool[activeQuestionIndex];
  const card = document.createElement("article");
  card.className = "question-card";

  const stats = document.createElement("div");
  stats.className = "quick-stats";
  stats.innerHTML = `
    <span class="stat-chip">Answered: ${answeredCount}</span>
    <span class="stat-chip">Remaining: ${remainingCount}</span>
    <span class="stat-chip">Flagged: ${flaggedQuestions.size}</span>
  `;
  card.appendChild(stats);

  const title = document.createElement("p");
  title.className = "question-title";
  title.textContent = `${activeQuestionIndex + 1}. ${q.question}`;
  card.appendChild(title);

  const optionsWrap = document.createElement("div");
  optionsWrap.className = "options";

  q.options.forEach((opt, oi) => {
    const label = document.createElement("label");
    label.className = "option";

    const input = document.createElement("input");
    input.type = "radio";
    input.name = q.id;
    input.value = String(oi);
    input.checked = userAnswers[q.id] === oi;
    input.addEventListener("change", () => {
      userAnswers[q.id] = oi;
      nextBtn.disabled = false;
    });

    const text = document.createElement("span");
    text.textContent = opt;

    label.appendChild(input);
    label.appendChild(text);
    optionsWrap.appendChild(label);
  });

  const nextBtn = document.createElement("button");
  nextBtn.className = "primary-btn";
  nextBtn.textContent = activeQuestionIndex === testQuestionPool.length - 1 ? "Finish Test" : "Next Question";
  nextBtn.disabled = typeof userAnswers[q.id] !== "number";
  nextBtn.addEventListener("click", () => {
    if (typeof userAnswers[q.id] !== "number") {
      return;
    }
    activeQuestionIndex += 1;
    renderTestMode();
  });

  const prevBtn = document.createElement("button");
  prevBtn.className = "ghost-btn";
  prevBtn.textContent = "Previous";
  prevBtn.disabled = activeQuestionIndex === 0;
  prevBtn.addEventListener("click", () => {
    if (activeQuestionIndex > 0) {
      activeQuestionIndex -= 1;
      renderTestMode();
    }
  });

  const skipBtn = document.createElement("button");
  skipBtn.className = "ghost-btn";
  skipBtn.textContent = activeQuestionIndex === testQuestionPool.length - 1 ? "Finish Now" : "Skip";
  skipBtn.addEventListener("click", () => {
    activeQuestionIndex += 1;
    renderTestMode();
  });

  const flagBtn = document.createElement("button");
  flagBtn.className = "ghost-btn";
  flagBtn.textContent = flaggedQuestions.has(q.id) ? "Unflag" : "Flag for Review";
  flagBtn.addEventListener("click", () => {
    if (flaggedQuestions.has(q.id)) {
      flaggedQuestions.delete(q.id);
    } else {
      flaggedQuestions.add(q.id);
    }
    renderTestMode();
  });

  const jumpInput = document.createElement("input");
  jumpInput.className = "control-input jump-input";
  jumpInput.type = "number";
  jumpInput.min = "1";
  jumpInput.max = String(testQuestionPool.length);
  jumpInput.placeholder = "Go to #";

  const jumpBtn = document.createElement("button");
  jumpBtn.className = "ghost-btn";
  jumpBtn.textContent = "Go";
  jumpBtn.addEventListener("click", () => {
    const requested = Number(jumpInput.value);
    if (Number.isInteger(requested) && requested >= 1 && requested <= testQuestionPool.length) {
      activeQuestionIndex = requested - 1;
      renderTestMode();
    }
  });

  const navRow = document.createElement("div");
  navRow.className = "question-nav";
  navRow.appendChild(prevBtn);
  navRow.appendChild(skipBtn);
  navRow.appendChild(flagBtn);
  navRow.appendChild(jumpInput);
  navRow.appendChild(jumpBtn);

  card.appendChild(optionsWrap);
  card.appendChild(navRow);
  card.appendChild(nextBtn);
  testQuestionsEl.appendChild(card);
}

function submitTest() {
  const weekData = getCurrentWeekData();
  const total = testQuestionPool.length;
  let correct = 0;
  const wrongBreakdown = [];

  testQuestionPool.forEach((q, i) => {
    const user = userAnswers[q.id];
    if (user === q.answerIndex) {
      correct += 1;
    } else {
      wrongBreakdown.push({
        no: i + 1,
        question: q.question,
        userAnswer: typeof user === "number" ? q.options[user] : "Not answered",
        correctAnswer: q.options[q.answerIndex]
      });
    }
  });

  const percent = ((correct / total) * 100).toFixed(1);
  const flaggedCount = testQuestionPool.filter((q) => flaggedQuestions.has(q.id)).length;
  const wrongHtml = wrongBreakdown
    .map(
      (w) => `
        <article class="question-card">
          <p class="question-title">Q${w.no}. ${w.question}</p>
          <p><span class="badge wrong">Your answer</span> ${w.userAnswer}</p>
          <p><span class="badge correct">Correct answer</span> ${w.correctAnswer}</p>
        </article>
      `
    )
    .join("");

  const resultTitle = testScope === "all" ? "All Questions Result" : `Week ${weekData.week} Result`;
  const sortedWeeks = getSortedWeeks();
  const currentWeekPos = sortedWeeks.indexOf(weekData.week);
  const hasNextWeek = testScope !== "all" && currentWeekPos !== -1 && currentWeekPos < sortedWeeks.length - 1;
  const nextWeek = hasNextWeek ? sortedWeeks[currentWeekPos + 1] : null;
  resultBoxEl.innerHTML = `
    <h3>${resultTitle}</h3>
    <p><strong>Score:</strong> ${correct}/${total} (${percent}%)</p>
    <p><strong>Flagged for review:</strong> ${flaggedCount}</p>
    <hr />
    <h4>Analysis</h4>
    ${
      wrongBreakdown.length === 0
        ? "<p>Excellent! All answers are correct.</p>"
        : `<p>Review these ${wrongBreakdown.length} question(s):</p>${wrongHtml}`
    }
    <div class="question-nav">
      ${hasNextWeek ? `<button id="nextWeekBtn" class="primary-btn">Move to Week ${nextWeek} Test</button>` : ""}
      <button id="otherWeekBtn" class="ghost-btn">Choose Other Week</button>
      <button id="retakeBtn" class="ghost-btn">Retake</button>
    </div>
  `;
  resultBoxEl.classList.remove("hidden");
  testProgressEl.textContent = `Completed ${total} of ${total}`;
  testQuestionsEl.innerHTML = `<p>Test completed. Check your score and analysis below.</p>`;
  startTestBtn.classList.remove("hidden");
  startTestBtn.textContent = "Configure & Start Test";
  updateTestingLayout();

  const retakeBtn = document.getElementById("retakeBtn");
  const otherWeekBtn = document.getElementById("otherWeekBtn");
  const nextWeekBtn = document.getElementById("nextWeekBtn");

  if (retakeBtn) {
    retakeBtn.addEventListener("click", () => {
      openTestSetupModal();
    });
  }

  if (otherWeekBtn) {
    otherWeekBtn.addEventListener("click", () => {
      testStarted = false;
      testQuestionPool = [];
      resultBoxEl.classList.add("hidden");
      updateTestingLayout();
      setMode("study");
    });
  }

  if (nextWeekBtn && nextWeek !== null) {
    nextWeekBtn.addEventListener("click", () => {
      selectedWeek = nextWeek;
      userAnswers = {};
      flaggedQuestions = new Set();
      activeQuestionIndex = 0;
      testStarted = false;
      testQuestionPool = [];
      closeTestSetupModal();
      resultBoxEl.classList.add("hidden");
      setMode("test");
      openTestSetupModal();
    });
  }
}

function getAllQuestions() {
  return questionBank.flatMap((weekData) =>
    weekData.questions.map((q, idx) => ({
      ...q,
      id: `${q.id}-all-${idx}`,
      sourceWeek: weekData.week
    }))
  );
}

function shuffleArray(items) {
  const arr = [...items];
  for (let i = arr.length - 1; i > 0; i -= 1) {
    const j = Math.floor(Math.random() * (i + 1));
    const temp = arr[i];
    arr[i] = arr[j];
    arr[j] = temp;
  }
  return arr;
}

function setMode(mode) {
  currentMode = mode;
  studyModeBtn.classList.toggle("active", mode === "study");
  testModeBtn.classList.toggle("active", mode === "test");
  studySectionEl.classList.toggle("hidden", mode !== "study");
  testSectionEl.classList.toggle("hidden", mode !== "test");
  renderAll();
}

function applyTheme(theme) {
  const isLight = theme === "light";
  document.body.classList.toggle("light-theme", isLight);
  themeToggleBtn.textContent = isLight ? "Dark Theme" : "Light Theme";
  localStorage.setItem("quiz-theme", theme);
}

function startConfiguredTest() {
  testStarted = true;
  userAnswers = {};
  flaggedQuestions = new Set();
  activeQuestionIndex = 0;
  testScope = testScopeSelect.value;
  shuffleEnabled = shuffleToggle.checked;
  if (testScope !== "all") {
    selectedWeek = Number(testWeekSelect.value);
  }
  const baseQuestions = testScope === "all" ? getAllQuestions() : getCurrentWeekData().questions.map((q) => ({ ...q }));
  testQuestionPool = shuffleEnabled ? shuffleArray(baseQuestions) : baseQuestions;
  startTestBtn.textContent = "Configure & Start Test";
  resultBoxEl.classList.add("hidden");
  closeTestSetupModal();
  renderHeader();
  renderTestMode();
}

function renderAll() {
  updateTestingLayout();
  renderWeekList();
  renderHeader();
  if (currentMode === "study") {
    renderStudyMode();
  } else {
    renderTestMode();
  }
}

